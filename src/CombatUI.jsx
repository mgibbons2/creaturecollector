function hpPercent(cur, max) { return Math.max(0, Math.min(100, (cur / max) * 100)); }
function hpBarColor(pct) {
  if (pct > 50) return "#40C850";
  if (pct > 20) return "#F8D030";
  return "#F85840";
}

